    public function getTopRatedThisWeek()
    {
        $connection = $this->getEntityManager()->getConnection();
        $sql = "SELECT movie_id, COUNT(id) AS total_reviews, AVG(score) AS average_score
                FROM review
                WHERE date_reviewed BETWEEN DATE_SUB(CURDATE(), INTERVAL 7 DAY) AND CURDATE()
                GROUP BY movie_id
                ORDER BY total_reviews DESC, average_score DESC";
        $resultSet = $connection->executeQuery($sql);
        return $resultSet->fetchAllAssociative();
    }

